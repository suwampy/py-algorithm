## 복잡도 종류
# 복잡도 종류 : 계산에 드는 복잡도에 따라 문제들을 분류해놓은 집합
# 1. P : 계산의 각 단계에서 단 한가지 가능성만을 고려할 수 있는 알고리즘으로
#        다항 시간 안에 풀 수 있는 문제.
#        어떤 문제를 결정론적 문제로 바꿀 수 있다면 그 결과는 P 에 속하게 됨

# 2. NP : 비결정론적 튜링 기계로 다항 시간 안에 풀 수있는 판정 문제의 집합
#         각 계산 단계에서 여러 가능성을 동시에 고려할 수 있는 알고리즘을 통해
#         다항 시간 안에 풀 수 있음